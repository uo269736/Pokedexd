package models;

public class Nombre {

    private String name;
    private Lenguaje language;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Lenguaje getLanguage() {
        return language;
    }

    public void setLanguage(Lenguaje languaje) {
        this.language = languaje;
    }
}
