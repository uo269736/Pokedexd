package models;

public class DescripcionO {

    private String text;
    private Lenguaje language;

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Lenguaje getLenguage() {
        return language;
    }

    public void setLenguage(Lenguaje languaje) {
        this.language = languaje;
    }
}
